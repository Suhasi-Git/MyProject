package Assignment;

public class Practise_Assignment_2 {

	public static void main(String[] args) {
		
		//for loop example: Sending Multiple Emails
		
		for (int i=1; i<=10; i++)
		{
			System.out.println("Email Sent");
		}

		//do while loop: sending Multiple Emails
		
		int j=30;
		
		do
		{
			System.out.println(j);
			j--;
		}
		while (j>=1);
		
		
	}

}
