package day3;

public class Assignment_Operators {

	public static void main(String[] args) {
		
		//Assignment Operators += -+ *= /= %=
		
		int a=10;
		
		//Example for +=
		a+=5;
		System.out.println(a); //15
		
		//Example for -=
		a-=5;
		System.out.println(a);  //10
		
		//Example for *=
		a*=5;
		System.out.println(a);  //50
		
		//Example for /=
		a/=5;
		System.out.println(a);  //10
		
		//Example for %=
		a%=5;
		System.out.println(a);  //0

	}

}
